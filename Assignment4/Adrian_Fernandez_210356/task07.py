"""**TASK 7.2a: List all individuals of "Person" with RDFLib (remember the subClasses). Return the individual URIs in a list called "individuals"**

"""

ns = Namespace("http://oeg.fi.upm.es/def/people#")

# Diccionario que mapea cada clase a sus individuos
class_to_individuals = {}
for cls in g.subjects(RDF.type, RDFS.Class):
    class_to_individuals[cls] = list(g.subjects(RDF.type, cls))

# Funci√≥n recursiva para obtener individuos de Person y sus subclases
def get_individuals(cls):
    inds = set(class_to_individuals.get(cls, []))
    for subcls in g.subjects(RDFS.subClassOf, cls):
        inds.update(get_individuals(subcls))
    return inds

# Lista final de individuos
individuals = list(get_individuals(ns.Person))
# visualize results
for i in individuals:
  print(i)

# validation. Do not remove
report.validate_07_02a(individuals)

"""**TASK 7.2b: Repeat the same exercise in SPARQL, returning the individual URIs in a variable ?ind**"""

query = """
PREFIX rdf: <http://www.w3.org/1999/02/22-rdf-syntax-ns#>
PREFIX rdfs: <http://www.w3.org/2000/01/rdf-schema#>
PREFIX peo: <http://oeg.fi.upm.es/def/people#>

SELECT DISTINCT ?ind
WHERE {
    ?cls rdfs:subClassOf* peo:Person .
    ?ind rdf:type ?cls .
}
"""

for r in g.query(query):
  print(r.ind)
